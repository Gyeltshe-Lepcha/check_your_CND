"use client";

import { AlertIcon, InfoIcon } from "@/components/icons/Icons";
import { BmiCard } from "@/components/results/BmiCard";
import { InsightsCard } from "@/components/results/InsightsCard";
import { ResultActions } from "@/components/results/ResultActions";
import { RiskGauge } from "@/components/results/RiskGauge";
import { RiskTabs } from "@/components/results/RiskTabs";
import { SummaryCard } from "@/components/results/SummaryCard";

function createReport(result) {
  const { summary, bmi, riskScore, band } = result;

  return [
    "HealthCheck NCD Risk Report",
    "",
    `Risk cluster: ${band.label}`,
    `Risk score: ${riskScore}%`,
    `Age: ${summary.age} yrs`,
    `Weight: ${summary.weight} kg`,
    `Height: ${summary.height} cm`,
    `Blood pressure: ${summary.systolic}/${summary.diastolic}`,
    `Activity level: ${summary.activityLevel}`,
    `Diet type: ${summary.dietType}`,
    `BMI: ${bmi.value} (${bmi.status})`,
    "",
    "Medical disclaimer: This assessment is for informational purposes only and is not a medical diagnosis.",
  ].join("\n");
}

export function RiskResults({ result, onRetake }) {
  const handleDownload = () => {
    const blob = new Blob([createReport(result)], { type: "text/plain" });
    const url = URL.createObjectURL(blob);
    const anchor = document.createElement("a");
    anchor.href = url;
    anchor.download = "healthcheck-risk-report.txt";
    anchor.click();
    URL.revokeObjectURL(url);
  };

  const handleShare = async () => {
    const text = `My HealthCheck result: ${result.riskScore}% ${result.band.label}`;

    if (navigator.share) {
      await navigator.share({ title: "HealthCheck Risk Result", text });
      return;
    }

    await navigator.clipboard.writeText(text);
  };

  return (
    <main className="min-h-screen bg-[#eef4fb] px-5 py-10 text-[#061633] sm:px-8">
      <RiskTabs active={result.band.key} />

      <section className="mx-auto mt-[30px] max-w-[1380px] overflow-hidden rounded-[28px] border border-amber-300 bg-white shadow-[0_14px_32px_rgba(15,23,42,0.08)]">
        <div className="flex items-center gap-3 bg-amber-50 px-[30px] py-[17px] text-[15px] font-black text-orange-700">
          <AlertIcon className="size-5 shrink-0" />
          <span>{result.band.alert}</span>
        </div>

        <div className="grid gap-8 px-[50px] py-[50px] lg:grid-cols-[330px_minmax(0,1fr)] max-sm:px-5">
          <div className="flex flex-col items-center gap-[30px]">
            <RiskGauge score={result.riskScore} band={result.band} />
            <ResultActions
              compact
              onDownload={handleDownload}
              onShare={handleShare}
              onRetake={onRetake}
            />
          </div>
          <SummaryCard result={result} />
        </div>
      </section>

      <section className="mx-auto mt-[30px] grid max-w-[1380px] grid-cols-1 items-start gap-[30px] xl:grid-cols-[minmax(340px,443px)_minmax(0,1fr)]">
        <BmiCard bmi={result.bmi} />
        <InsightsCard />
      </section>

      <section className="mx-auto mt-[30px] max-w-[1380px]">
        <ResultActions
          onDownload={handleDownload}
          onShare={handleShare}
          onRetake={onRetake}
        />
      </section>

      <section className="mx-auto mt-[30px] flex max-w-[1380px] gap-4 rounded-[18px] border border-[#dce6f2] bg-white px-[22px] py-[20px] text-[#64789d]">
        <span className="grid size-10 shrink-0 place-items-center rounded-[12px] bg-blue-50 text-blue-500">
          <InfoIcon className="size-5" />
        </span>
        <p className="text-[15px] leading-6">
          <strong className="block text-[16px] text-[#2f4261]">Medical Disclaimer</strong>
          This assessment is for informational purposes only and is not a medical
          diagnosis. Results are generated by an AI model and should not replace
          professional medical advice. Please consult a qualified healthcare
          provider before making any health-related decisions.
        </p>
      </section>
    </main>
  );
}
